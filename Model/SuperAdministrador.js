const { Schema, model } = require('mongoose');

const SuperAdministrador = Schema({
    nombre: {
        type: String,
        required: true,
    },
    apellido: {
        type: String,
        required: true,
    },
    email: {
        type: String,
        required: true,
        unique: true,
    },
    password: {
        type: String,
        required: true,
    },
})
module.exports = model("SuperAdministrador",SuperAdministrador)
